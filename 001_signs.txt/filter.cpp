#include <bits/stdc++.h>
using namespace std;
map<char, string> morseAlphabet{
	{'a', ".-"},	{'0', "-----"},
	{'b', "-..."},	{'1', ".----"},
	{'c', "-.-."},	{'2', "..---"},
	{'d', "-.."},	{'3', "...--"},
	{'e', "."},		{'4', "....-"},
	{'f', "..-."},	{'5', "....."},
	{'g', "--."},	{'6', "-...."},
	{'h', "...."},	{'7', "--..."},
	{'i', ".."},	{'8', "---.."},
	{'j', ".---"},	{'9', "----."},
	{'k', "-.-"},	{'.', ".-.-.-"},
	{'l', ".-.."},	{',', "--..--"},
	{'m', "--"},	{'?', "..--.."},
	{'n', "-."},	{'\'', ".----."},
	{'o', "---"},	{'!', "-.-.--"},
	{'p', ".--."},	{'/', "-..-."},
	{'q', ".--."},	{'(', "-.--."},
	{'r', ".-."},	{')', "-.--.-"},
	{'s', "..."},	{'&', ".-..."},
	{'t', "-"},		{':', "---..."},
	{'u', "..-"},	{';', "-.-.-."},
	{'v', "...-"},	{'=', "-...-"},
	{'w', ".--"},	{'+', ".-.-."},
	{'x', "-..-"},	{'-', "-....-"},
	{'y', "-.--"},	{'\"', ".-..-."},
	{'z', "--.."},
};

string mors(string st){
	string m = "";
	for(unsigned i = 0; i < st.length(); i++){
		m += morseAlphabet[st[i]];
	}
	return m;
}

int main(){
	// ifstream fin("wordsFiltered2.txt", ofstream::out);
	// ofstream fout("wordsFiltered2_morse.txt", ofstream::out);
	//
	string st;
	while(cin >> st){
		cout << mors(st) << '\n';
	}
	cout << "\nDone.";
}